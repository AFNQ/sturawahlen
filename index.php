<h3>Übersicht</h3>

<div class="full_box">

	Die Wahlen zum Studierendenrat, den Fachschaftsräten und den studentischen VertreterInnen im Senat und den Fakultätsräten finden jährlich im Juni statt. Zur Wahl stellen können sich alle an der Otto-von-Guericke-Universität immatrikulierten Studierenden. Mit der Wahl gibst du den KommilitonInnen deine Stimmen, von denen du denkst, dass sie die Interessen der Studierenden am wirksamsten vertreten und durchsetzen können. Wie bei vielen anderen Wahlen stehen Gruppen bereit, um die verschiedenen Interessen, politischen Einstellungen und Wünsche in den Gremien zu artikulieren und umzusetzen.
	<br><br>
	Die VertreterInnen für die Gremien werden nach personalisierter Verhältniswahl gewählt. Die KandidatInnen sind dabei in Listen organisiert. Die Listen bekommen dann je nach Stimmenzahl eine Anzahl von Sitzen, welche von den KandidatInnen mit den meisten Stimmen besetzt werden. Gibt es für ein Gremium nur eine Liste, werden die VertreterInnen direkt gewählt.

</div>

